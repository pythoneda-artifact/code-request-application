import asyncio
from dbus_next import BusType
from pythoneda.shared.application import enable, PythonEDA
from pythoneda.shared.artifact.events.infrastructure.dbus import DbusChangeStaged
from pythoneda.artifact.code_request.infrastructure.dbus import (
    CodeRequestDbusSignalEmitter,
)


@enable(
    CodeRequestDbusSignalEmitter,
    events=[{"event-class": DbusChangeStaged, "bus-type": BusType.SYSTEM}],
)
class CodeRequestApp(PythonEDA):
    """
    Runs the PythonEDA within a code request.

    Class name: CodeRequestApp

    Responsibilities:
        - Runs PythonEDA, providing a hook to run specific tasks.

    Collaborators:
        - None
    """

    def __init__(self):
        """
        Creates a new CodeRequestApp instance.
        """
        # code_request_banner is automatically generated by pythoneda-artifact/code-request-application
        try:
            from pythoneda.artifact.code_request.application.code_request_banner import (
                CodeRequestBanner,
            )

            banner = CodeRequestBanner()
        except ImportError:
            banner = None
        super().__init__(banner, __file__)

    async def after_bootstrap(self):
        """
        Hook to run code after the bootstrap process.
        """
        await self.emit_event()

    async def emit_event(self):
        """
        Emits events as part of running the code request.
        """
        pass


if __name__ == "__main__":
    asyncio.run(CodeRequestApp.main())
